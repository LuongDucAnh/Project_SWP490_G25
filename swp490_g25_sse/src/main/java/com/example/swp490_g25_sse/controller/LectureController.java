package com.example.swp490_g25_sse.controller;

import com.example.swp490_g25_sse.model.CourseLecture;
import com.example.swp490_g25_sse.service.LectureService;
import java.util.List;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

/**
 *
 * @author ADMIN
 */
@RestController
@RequestMapping(path = "create_lecture")
public class LectureController {

    @Autowired
    private final LectureService lectureService;

    public LectureController(LectureService lectureService) {
        this.lectureService = lectureService;
    }

    @GetMapping
    public List<CourseLecture> display() {
        return lectureService.display();
    }
}
